package com.baidu.mapapi.clusterutil.projection;

public class SecondBean {
	private String name;
	private String id;
	// 加载状态 state 0:加载中 1:加载完成
	private int state;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

}
